    private void btnDarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarActionPerformed
        FileReader leedor = null;
        try {
            String cH = "";
            leedor = new FileReader("Historial.txt");
            BufferedReader jajaja = new BufferedReader(leedor);


            try {
                String linea = jajaja.readLine();
                while ((txthistorial = linea) != null) {
                    cH += linea + "\n";
                    linea = jajaja.readLine();
                }
                if (!txtHistorial.getText().equals(txthistorial)) {
                    txtHistorial.setText("");
                    txtHistorial.setText(cH);
                }

            } catch (IOException ex) {
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                leedor.close();
            } catch (IOException ex) {

                Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDarActionPerformed

